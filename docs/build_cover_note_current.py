def copy_run_format(source_run, target_run) -> None:
    target_run.bold = source_run.bold
    target_run.italic = source_run.italic
    target_run.underline = source_run.underline
    target_run.font.name = source_run.font.name
    target_run.font.size = source_run.font.size
    target_run.font.color.rgb = source_run.font.color.rgb
    target_run.font.highlight_color = source_run.font.highlight_color
    target_run.font.all_caps = source_run.font.all_caps
    target_run.font.small_caps = source_run.font.small_caps


def replace_paragraph_text(paragraph, text: str) -> None:
    source_run = paragraph.runs[0] if paragraph.runs else None
    p_pr = paragraph._p.get_or_add_pPr()
    num_pr = p_pr.find(qn("w:numPr"))
    if num_pr is not None:
        p_pr.remove(num_pr)
    ind = p_pr.find(qn("w:ind"))
    if ind is not None:
        p_pr.remove(ind)
    paragraph.clear()
    run = paragraph.add_run(text)
    if source_run is not None:
        copy_run_format(source_run, run)


def clone_paragraph_after(paragraph):
    new_p = deepcopy(paragraph._p)
    paragraph._p.addnext(new_p)
    return paragraph.__class__(new_p, paragraph._parent)


def main() -> None:
    doc = Document(TEMPLATE_PATH)
    nonempty = [p for p in doc.paragraphs if p.text.strip()]

    if len(CONTENT) > len(nonempty):
        prototype = nonempty[-1]
        last_paragraph = prototype
        for _ in range(len(CONTENT) - len(nonempty)):
            last_paragraph = clone_paragraph_after(last_paragraph)
            nonempty.append(last_paragraph)

    for paragraph, text in zip(nonempty, CONTENT):
        replace_paragraph_text(paragraph, text)

    for paragraph in nonempty[len(CONTENT) :]:
        replace_paragraph_text(paragraph, "")

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    doc.save(OUTPUT_PATH)
    print(OUTPUT_PATH)


if __name__ == "__main__":
    main()
